//! Headless TypeScript bindings exporter.
//!
//! Run with: `cargo run -p agentmix-types --features export --bin export-bindings`
//! Writes `src/types/generated.ts` from the Rust models. This binary is
//! tauri-free (no WebView2/wry), so it runs in CI without a GUI.

use std::fs;
use std::path::Path;

use specta::Types;
use specta_typescript::Typescript;

fn main() {
    // Registering SourceProject pulls in Skill and the health/category enums;
    // the conflict types are registered explicitly (no struct references them).
    let types = Types::default()
        .register::<agentmix_types::SourceProject>()
        .register::<agentmix_types::ConflictCandidate>()
        .register::<agentmix_types::ExportConflict>()
        .register::<agentmix_types::ExportPlan>()
        .register::<agentmix_types::ExportRequestItem>()
        .register::<agentmix_types::ExecutionReport>()
        .register::<agentmix_types::SkillSecurityReport>();

    let rendered = Typescript::default()
        .header(
            "// AUTO-GENERATED by `cargo run -p agentmix-types --features export --bin export-bindings`.\n\
             // Source of truth: src-tauri/crates/agentmix-types/src/lib.rs. Do not edit by hand.\n",
        )
        // specta_serde::Format applies serde attributes (e.g. rename_all) during export.
        .export(&types, specta_serde::Format)
        .expect("failed to render TypeScript from Rust models");

    let out = Path::new(env!("CARGO_MANIFEST_DIR")).join("../../../src/types/generated.ts");
    if let Some(dir) = out.parent() {
        fs::create_dir_all(dir).expect("failed to create src/types directory");
    }
    fs::write(&out, rendered).expect("failed to write generated.ts");

    println!("exported bindings to {}", out.display());
}
